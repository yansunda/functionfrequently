它是一个软件，可以在内存重保存数据，从而快速读取数据。
好多软件都是服务器软件，只支持Linux系统【程序员专用】
memcache只有Linux系统的安装包。
①什么是memcache？
memory cache  内存缓存
MemCache是一个高性能的分布式的内存对象缓存系统，通过在内存里维护一个统一的巨大的hash表，它能够用来存储各种格式的数据，包括图像、视频、文件以及数据库检索的结果等。简单的说就是将数据调用到内存中，然后从内存中读取，从而大大提高读取速度。
把网站中需要频繁操作数据库的数据放到内存中，用的时候直接读取就行。


1）网站中大量的读数据库操作会增数据库的负载，可以把数据缓存到Memcache中以减少数据库压力。
2）把一些需要复杂算法才能计算出来的结果缓存到Memcache中以减少CPU的压力。
3）利用memcached可以缓存session数据。【我们平时的session文件是存在服务器的硬盘中的】【服务器对应的session文件名存在于浏览器中cookie】
如果是大网站，比如网站的并发量在200，那么每秒钟都要从硬盘中打开200个文件，那么就会很慢。这个慢就是在硬盘IO中了。因此大网站硬盘的I/O是一定要考虑的。【这个是必用的】
如果想更加了解磁盘的I/O，可以到http://blog.csdn.net/letterwuyu/article/details/53542291中了解。
4）缓存一些很小但是被频繁访问的文件和数据。【服务器的内存很小，不贵】
1024G = 1T 1024T = 1P  
是电脑如果重启了，数据就没有了，所以内存不适合保存大量的数据。

比如社区交友类网站，这种网站的数据都是实时发生变化的，不容易加上缓存，不容易优化，那么它的前端是PHP，数据库和mysql之间加了很多memcache，PHP就操作memcache。那么电脑出现故障消失了。做法是同一个数据存两份【防止数据消失】，后台有个脚本，定期把内存回写到硬盘中。【那么这种架构成本比较高】那么有些网站不能加缓存还要快那么就要加上memcache服务器。
一般的网站加上缓存就行。
