④操作memcache【就是一套函数，手册中有】
	连接：短连接【脚本执行完之后服务器就会释放连接资源】
					$m = new Memcache;
					$m->connect('localhost',11211);
		  长连接【脚本执行完之后内存不释放，下次连接的时候更快。但占内存】
		  		$m = new Memcache;
		  		$m->pconnect('localhost',11211);
	断开连接//脚本执行完就自动关闭，但如果是长连接，关闭也不会释放。
			$m->close();
	保存数据
	$m->add('data',$data,10);//前两个参数和assign一样,第三个参数：过期时间
	说明：1、即使不设置过期时间，也不能超过30天
		  2、存储的每个数据的大小不能操作1M【可以操作小于1M的模块】
		  3、如果添加时这个数据已经存在【数据名字已经存在（第一个参数）】，那么这个数据会添加失败。
	取数据
		$m->get('data')//数据名
	删除数据
		$m->delete('data',30)//第一个参数：数据名，第二个参数：多少秒之后删除
	更新值
		$m->set('data',$data,10) //和assign相同,设置过期时间，默认30天
		说明：如果没有值那么就添加，如果有值就更新。
	清空表中的所有数据
		$m->flush()
	memcache中的值加几  【浏览量等】比如秒杀就不能实时操作数据库
		$m->increment('data',2)//把data的值加上2
	获取服务器的基本信息
	$ret = $m->getStats('localhost',11211) 可以打印查看
	设置关闭（开启）数据压缩【一般不设置】
		memcache中存储数据的时候会先压缩。所以实际上可以存储大于1M的数据
		可以设置不压缩
		$m->setCompressThreshold(100 * 1024 * 1024)//数据只有大于100M的时候才压缩
	分布式存储【有可能一台服务器不够用，所以在公司里会搭建多台memcache服务器】
	那么到底存到那台服务器，到底去哪里取出数据呢。
	memcache直接支持分布式存储，也就是有几台服务器就行，memcache会自动选择一台并存储，取数据的时候到这上面去取就行。
	连接数据库的语句改为
	$m->addServer('ip地址',11211)
	$m->addServer('ip地址',11211)
	$m->addServer('ip地址',11211)
	$m->addServer('ip地址',11211)
	其他的都不变。



	到此memcache就可以根据需求来用了。
	



	





